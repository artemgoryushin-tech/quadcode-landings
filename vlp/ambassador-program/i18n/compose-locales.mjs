const pt = JSON.parse(fs.readFileSync(path.join(__dirname, "pt.json"), "utf8"));

function writeLocale(name, targets) {
  if (targets.length !== pt.length) {
    throw new Error(`${name}: expected ${pt.length} targets, got ${targets.length}`);
  }
  const pairs = pt.map((row, i) => [row[0], targets[i]]);
  fs.writeFileSync(path.join(__dirname, `${name}.json`), JSON.stringify(pairs), "utf8");
  console.log("wrote", name + ".json");
}

